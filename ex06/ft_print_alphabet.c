void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}

void	ft_print_alphabet(void)
{
	ft_putstr("abcdefghijklmnopqrstuvwxyz");
}
